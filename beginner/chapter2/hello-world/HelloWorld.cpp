#include <iostream>
using namespace std;
// std:: specifies that the name cout is to be found in the standard-library,
// the using namespace std make names from std visible without using std::

int main()
{
    cout << "Hello, World!" << endl << "hahaha" << endl; // "<<" literally means "put to", in this case << is putting the value "Hello, World!" into the 
							 // standard output stream "cout"
    return 0;
}
